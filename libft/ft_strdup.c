#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		size;
	int		i;
	char	*dest;

	size = 0;
	i = 0;
	while (src[size])
		size++;
	if (!(dest = (char *)malloc((sizeof(char) * size) + 1)))
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
